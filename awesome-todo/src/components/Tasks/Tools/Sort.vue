<template>
  <q-select
    emit-value
    map-options
    filled
    v-model="sortBy"
    :options="options"
    label="Sort by"
    stack-label
    class="col q-ml-sm"
  />
</template>

<script>
import { mapState, mapActions } from "vuex";

export default {
  data() {
    return {
      options: [
        {
          label: "Name",
          value: "name"
        },
        {
          label: "Date",
          value: "dueDate"
        }
      ],

      dense: false,
      denseOpts: false
    };
  },

  methods: {
    ...mapActions("tasks", ["setSort"])
  },

  computed: {
    ...mapState("tasks", ["sort"]),
    sortBy: {
      get() {
        return this.sort;
      },
      set(value) {
          this.setSort(value);
      }
    }
  }
};
</script>

<style scoped>
.q-select {
  flex: 0 0 160px;
}
</style>
